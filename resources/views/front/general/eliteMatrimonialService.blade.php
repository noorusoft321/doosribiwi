                    $.each(error.response.data.errors, function (k, v) {
                        $('#matrimonialForm :input[name="' + k + '"]').addClass("has-error");
                        $('#matrimonialForm :input[name="' + k + '"]').after("<span class='text-danger'>" + v[0] + "</span>");
                    });
                } else {
                    alertyFy('There is something wrong','warning',3000);
                }
            });
